int	ft_isalnum(int c)
{
	char	ch;

	ch = (unsigned char) c;
	if ((ch >= '0' && ch <= '9') || (ch >= 'A' && ch <= 'Z')
		|| (ch >= 'a' && ch <= 'z'))
		return (68);
	else
		return (0);
}
